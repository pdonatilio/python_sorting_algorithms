# Tim Sort Examples
# Example to Ordenate an array using the sort function
# Complexity:
#   Worst Case: O(n Log(n))*
#   Best Case: O(log2(n!))*
#   *based on wikipedia

# Sorting numbers
items = [20, 3, 4, 10]
items.sort()
print(items)
items.sort(reverse=True)
print(items)

# Sorting names
names = ['Viviane', 'Paulo', 'Alex', 'Laz']
names.sort()
print(names)
names.sort(reverse=True)
print(names)
